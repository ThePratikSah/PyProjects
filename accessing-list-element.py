name = ['anjali', 'sneha', 'nitika', 'pratik', 'manisha']

#list with string element
print(name)
#accessing element using list index value
print(name[3])   #printing element pratik
print(name[4])  #printing element manisha

#accessing element using negative indexing
print(name[-5]) #printing element anjali
print(name[-2]) #printing element pratik

#accesing element using slicing and list indexing
#we use : for slicing
print(name[1:4]) #printing element between a range 1 to 3
print(name[1:3]) #printing element between a range 1 to 2

#accessing element using slicing and negative indexing
print(name[-4:-1]) #printing element between a range -4 to -2
print(name[-5:-3]) #printing element between a range -5 to -4
