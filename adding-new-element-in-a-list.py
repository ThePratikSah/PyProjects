animal = [] #empty list
print(animal)

#assigning new element
animal = ['cat', 'dog', 'lion']
print(animal)

#changing the value of any index
animal[1] = 'tiger'
print(animal[1])

#adding element using append() method
animal.append('cow')
print(animal)

#adding element using extend() method
animal.extend(['parrot', 'peacock'])
print(animal)